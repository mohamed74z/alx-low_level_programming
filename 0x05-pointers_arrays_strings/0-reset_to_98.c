/**
 * reset_to_98 - updates the value of an integer to 98
 * @n: pointer to the integer to update
 *
 * This function takes a pointer to an integer and updates the value
 * of the integer to which it points to 98.
 */
void reset_to_98(int *n)
{
	*n = 98; /* Dereferencing the pointer and assigning 98 to its value */
}
